// Addition
let money = 500;
let reward = 1000;
let wallet_1 = money + reward;
console.log(wallet_1);
// Subtraction
let buyBurger = 300;
let wallet_2 = wallet_1 - buyBurger;
console.log(wallet_2);
//Multiplication
let pocketMoney = 10000;
let wallet_3 = pocketMoney * 4;
console.log(wallet_3);
// Division
let internetFee = 50;
let wallet_4 = wallet_3 / internetFee;
console.log(wallet_4);
// Module
let wallet_5 = wallet_2 % wallet_4;
console.log(wallet_5);
// Exponential
let eidi = 100;
let wallet_6 = eidi ** 3;
console.log(wallet_6);
