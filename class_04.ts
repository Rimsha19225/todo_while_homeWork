// Addition
let money : number = 500;
let reward : number = 1000;
let wallet_1 : number = money + reward;
console.log(wallet_1);

// Subtraction
let buyBurger : number = 300;
let wallet_2 : number = wallet_1 - buyBurger;
console.log(wallet_2);

//Multiplication
let pocketMoney : number = 10000;
let wallet_3 : number = pocketMoney * 4;
console.log(wallet_3);

// Division
let internetFee : number = 50;
let wallet_4 : number = wallet_3/internetFee;
console.log(wallet_4);

// Module
let wallet_5 : number = wallet_2 % wallet_4;
console.log(wallet_5);

// Exponential
let eidi : number = 100;
let wallet_6 : number = eidi**3;
console.log(wallet_6);